package _27_ThisKey;

public class Triangle {

	//private String Color;
	public int height, width;
	public int x, y;
	
	
	
	public Triangle(int height, int width, int x, int y) {
		this.height = height;
		this.width = width;
		this.x = x;
		this.y = y;
	}
	
	public Triangle() {
		this(5, 5, 3, 3);
	}
	
	
}
