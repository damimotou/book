// Продемонстрировать выполнение операции %
public class Modulus {
    public static void main(String[] args) {
        int i = 42;
        double d = 42.25;
        System.out.println(i + " % 10 = " + (i % 10));
        System.out.println(d + " % 10 = " + (d % 10));
    }
}
